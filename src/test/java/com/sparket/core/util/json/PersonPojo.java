package com.sparket.core.util.json;

public class PersonPojo {
	private String name;
	private String role;
	private String city;
	private AddressPojo address;

	public String getName() {
		return this.name;
	}

	public String getRole() {
		return role;
	}

	public String getCity() {
		return city;
	}

	public AddressPojo getAddress() {
		return address;
	}
}
